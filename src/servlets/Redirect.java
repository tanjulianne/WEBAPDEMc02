		//doGet(request, response);
		
		if (request.getParameter("path").equals("upload")) {
            RequestDispatcher rs = request.getRequestDispatcher("upload.html");
            rs.include(request, response);
		} else if (request.getParameter("path").equals("login")) {
			RequestDispatcher rs = request.getRequestDispatcher("login.jsp");
            rs.include(request, response);
		} else if (request.getParameter("path").equals("register")) {
			RequestDispatcher rs = request.getRequestDispatcher("register.jsp");
            rs.include(request, response);
		}
